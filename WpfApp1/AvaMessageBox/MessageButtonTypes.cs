namespace AvaMessageBox;

public enum MessageButtonTypes
{
    /// <summary>
    /// 没有按钮
    /// </summary>
    NoButton,

    /// <summary>
    /// 仅确定按钮
    /// </summary>
    OkOnly,

    /// <summary>
    /// 是+否按钮
    /// </summary>
    YesNo,

    /// <summary>
    /// 是+否+取消按钮
    /// </summary>
    YesNoCancel,

    /// <summary>
    /// 仅确定按钮
    /// </summary>
    OkCancel,

    /// <summary>
    /// 仅取消按钮
    /// </summary>
    CancelOnly
}